// スコアから4文字コードを計算する関数
export function calculateTypeCode(scores) {
  const { LQ, RD, CH, PF } = scores;
  
  const char1 = LQ >= 0 ? 'L' : 'Q';
  const char2 = RD >= 0 ? 'R' : 'D';
  const char3 = CH >= 0 ? 'C' : 'H';
  const char4 = PF >= 0 ? 'P' : 'F';
  
  return char1 + char2 + char3 + char4;
}
